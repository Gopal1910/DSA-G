// This program demonstrates all basic Java Data Types

import java.util.*;
public class DataTypesDemo {
    public static void main(String[] args) {

        // byte -> small integer value (range -128 to 127)
        byte b = 8;
        System.out.println("Byte value: " + b);

        // char -> single character
        char ch = 'a';
        System.out.println("Character value: " + ch);

        // boolean -> true / false
        boolean var = true;
        System.out.println("Boolean value: " + var);

        // float -> decimal number (write f at the end)
        float price = 10.5f;
        System.out.println("Float value: " + price);

        // int -> normal integer
        int number = 25;
        System.out.println("Integer value: " + number);

        // long -> large integer values
        long bigNumber = 123456789L;
        System.out.println("Long value: " + bigNumber);

        // double -> larger decimal values
        double rate = 99.99;
        System.out.println("Double value: " + rate);

        // short -> smaller integer than int
        short n = 240;
        System.out.println("Short value: " + n);
    }
}
