import java.util.*;

// Program to demonstrate Relational Operators
public class RelationalOperators {
    public static void main(String[] args) {

        int A = 10;
        int B = 5;

        // == (equal to)
        System.out.println("A == B : " + (A == B));

        // != (not equal to)
        System.out.println("A != B : " + (A != B));

        // >= (greater than equal to)
        System.out.println("A >= B : " + (A >= B));

        // <= (less than equal to)
        System.out.println("A <= B : " + (A <= B));

        // > (greater than)
        System.out.println("A > B : " + (A > B));

        // < (less than)
        System.out.println("A < B : " + (A < B));
    }
}
